package javabasic;

public class ExOperato1 {
	
	public static void main(String[] args) {
		
		int a = 1;
		int b = 2;
		int c = 3;
		int d = 4;
		int e = 0;
		
		e= a+b;
		System.out.println(e);
		e= d-a;
		System.out.println(e);
		c= b++;
		System.out.println(e);
		c= ++b;
		System.out.println(c);
		c= d--;
		System.out.println(c);
		c= --d;
		
	}

}
